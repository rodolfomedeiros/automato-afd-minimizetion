    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        automato = new Automato();
        
        //alocando estados do automato
        String[] estadoHash = jTextField1.getText().split(",");
        automato.setEstadoHash(new HashMap<>());
        for (String estadoHash1 : estadoHash) {
            automato.setEstadoHash(new Estado(estadoHash1));
        }
        
        //setando o estado inicial do automato
        String estadoInicial = jTextField2.getText();
        if(!automato.setEstadoInicial(estadoInicial)){
            jLabel9.setForeground(Color.red);
            jLabel9.setText("Estado Inical Errado!");
        }else{
            //setando os estados finais
            String[] estadoFinal = jTextField4.getText().split(",");
            if(estadoFinal[0].equals("")){
                jLabel9.setForeground(Color.red);
                jLabel9.setText("Informe pelo menos um estado final!");
            }else{
                for (String estadoFinal1 : estadoFinal) {
                    automato.setEstadoFinal(estadoFinal1);
                }
                
                //setando o alfabeto
                String[] alfabeto = jTextField3.getText().split(",");
                if(alfabeto[0].equals("")){
                    jLabel9.setForeground(Color.red);
                    jLabel9.setText("Informe o alfabeto!");
                }else{
                    for (String alfabeto1 : alfabeto) {
                        automato.setAlfabeto(alfabeto1);
                    }
                    
                    //set transicões...
                    String[] transicao = jTextArea1.getText().split("\n");
                    if(transicao[0].equals("")){
                        jLabel9.setForeground(Color.red);
                        jLabel9.setText("Informe as Transições!");
                    }else{
                        if(automato.setTransicao(transicao)){
                            jLabel9.setForeground(Color.GREEN);
                            jLabel9.setText("Este Autômato é um AFD!");
                        }else{
                            jLabel9.setForeground(Color.RED);
                            jLabel9.setText("Não é um AFD!");
                        }
                    }
                }
            } 
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

