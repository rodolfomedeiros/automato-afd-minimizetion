    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        String estados = "";
        String estadoInicial = "";
        String alfabeto = "";
        String estadosFinais = "";
        String transicao = "";
        
        try {
            FileReader nome = new FileReader("arquivos/"+jTextField6.getText());
            BufferedReader arq = new BufferedReader(nome);
            
            arq.readLine();//Estados
            estados = arq.readLine();
            arq.readLine();//Estado Inicial
            estadoInicial = arq.readLine();
            arq.readLine();//Alfabeto
            alfabeto = arq.readLine();
            arq.readLine();//Estados Finais
            estadosFinais = arq.readLine();
            arq.readLine();//Transições
            
            //lendo Transições
            while(arq.ready()){
                transicao = transicao + arq.readLine() + "\n";
            }
            
            arq.close();
            nome.close();
        } catch (Exception e) {
            System.err.printf("Erro na abertura do arquivo: %s.\n",
            e.getMessage());
        }
        
        jTextField1.setText(estados);
        jTextField2.setText(estadoInicial);
        jTextField3.setText(alfabeto);
        jTextField4.setText(estadosFinais);
        jTextArea1.setText(transicao);
    }//GEN-LAST:event_jButton6ActionPerformed

